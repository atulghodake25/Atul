package ControlSyudy;

public class ElseIfStudy {

	public static void main(String[] args) {
		int marks=60;
		
		if (marks>=66) {
			
		System.out.println("You are in dist");
			
		}
		else if (marks>=35) {
			System.out.println("You got pass class");
		}
		else if (marks>=50) {
			System.out.println("You are 1st pass");
		}
			
		else {
			System.out.println("You are fail");
		}


	}

}
